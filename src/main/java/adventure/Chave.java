
package adventure;

public class Chave {
    
    private Boolean alcansavel;

    Chave() {
    	alcansavel = false;
    }
    
    private String nome;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Boolean getAlcansavel() {
        return alcansavel;
    }

    public void setAlcansavel(Boolean alcansavel) {
        this.alcansavel = alcansavel;
	}
    
	
 }
