
package adventure;

public class Ouro {
    
    private Integer quantidade;
    private Boolean alcansavel;
    
    Ouro() {
    }

    public Integer getQuantidade() {
        return quantidade;
    }

    public void setQuantidade(Integer quantidade) {
        this.quantidade = quantidade;
    }
    
    public Boolean getAlcansavel() {
        return alcansavel;
    }

    public void setAlcansavel(Boolean alcansavel) {
        this.alcansavel = alcansavel;
    }
    
}
